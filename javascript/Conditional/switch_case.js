// switch case

let day = 'Sunday'
switch(day){
    case 'Monday':
        console.log('Today is Monday')
        break;

    case 'Tuesday':  
        console.log('Today is Tuesday')  
        break;

    case 'Wednesday':  
        console.log('Today is Wednesday')  
        break; 
    
    case 'Sunday':  
        console.log('Today is Sunday')  
        break; 

    default:   
        console.log('Nothing') 
}